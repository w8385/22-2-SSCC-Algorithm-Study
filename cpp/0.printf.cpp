#include <stdio.h>

int main(){
    // 기본 출력
    printf("Hello SSCC!\n");

    // 정수 자료형 및 출력
    int n = 1983;
    printf("%d\n", n);

    // 실수 자료형 및 출력
    float pi = 3.14;
    printf("%f\n", pi);

    // 문자 자료형 및 출력
    char c = 'c';
    printf("%c\n", c);

    // 특수 문자 출력
    printf("%% ");
    printf("\\ ");
    printf("\' ");
    printf("\" ");

    return 0;
}
/*
 * Hello SSCC!
 * 1983
 * 3.14
 * c
 */